package com.jrv.springbootandjavaconcepts;

/**
 *
 * To store the reusable variables as a constant.
 * E.g. public static final String KEY = "Value"; This can be used at anywhere in the project as Constants.KEY
 *
 * @author Jayarathinavel
 * @since 01/Aug/2022
 */
public final class Constants {

    private Constants(){
    }

    public static final String HELLO_WORLD = "Hello World !";
    public static final String ERROR = "ERROR";
    public static final String SUCCESS = "SUCCESS";
    public static final String LOGGED_IN_SUCCESSFULLY = "Logged-in Successfully";
    public static final String REGISTERED_SUCCESSFULLY = "Registered Successfully";


}
